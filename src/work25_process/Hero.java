	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getHp() {
		return hp;
	}
	
	// mp
	public void setMp(int mp) {
		this.mp = mp;
	}
	public int getmp() {
		return mp;
	}
	
	// atk
	public void setAtk(int atk) {
		this.atk = atk;
	}
	public int getAtk() {
		return atk;
	}
	
	// speed
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getSpeed() {
		return speed;
	}
	
	// def
	public void setDef(int def) {
		this.def = def;
	}
	public int getDef() {
		return def;
	}
